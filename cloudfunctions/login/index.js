/**
 * 在事务中原子自增 counter 集合的 seq 字段，返回新 id
 *
 * counter 集合结构（仅一条记录）：
 *   { key: "userId", seq: 60000 }
 *
 * 事务保证并发安全：读-改-写在同个事务内完成，杜绝重复 id
 *
 * @param {object} transaction 已开启的事务对象
 * @returns {Promise<number>} 自增后的新 id
 */
async function getNextUserId(transaction) {
  const counterCol = transaction.collection('counter');

  // 1. 读取当前计数
  const res = await counterCol.where({ key: 'userId' }).get();
  if (!res.data || res.data.length === 0) {
    throw new Error('counter 集合未初始化，请先手动插入 { key: "userId", seq: 60000 }');
  }

  const currentSeq = res.data[0].seq;
  const newSeq = currentSeq + 1;

  // 2. 原子更新
  await counterCol.where({ key: 'userId' }).update({
    data: { seq: newSeq },
  });

  return newSeq;
}

// ──────────────────────────────────────────────────────────
//  主入口
// ──────────────────────────────────────────────────────────

exports.main = async (event, context) => {
  // 获取微信上下文中的 openId（无需用户授权，静默获取）
  const wxContext = cloud.getWXContext();
  const openId = wxContext.OPENID;

  if (!openId) {
    return { code: -1, msg: '获取 openId 失败' };
  }

  const now = Date.now();
  const userCol = db.collection('user');

  try {
    // ── 情况 A：查询 open_id 是否已存在 ──
    const existRes = await userCol.where({ open_id: openId }).get();

    if (existRes.data && existRes.data.length > 0) {
      // 用户已存在，仅更新 update_time
      const user = existRes.data[0];
      await userCol.doc(user._id).update({
        data: { update_time: now },
      });

      return {
        code: 0,
        msg: '登录成功',
        isNew: false,
        data: {
          id: user.id,
          nickname: user.nickname,
          create_time: user.create_time,
          update_time: now,
          open_id: user.open_id,
        },
      };
    }

    // ── 情况 B：新用户，事务写入 ──
    const transaction = await db.startTransaction();

    let newId;
    let nickname;

    try {
      // ① 从 counter 集合原子自增获取新 id
      newId = await getNextUserId(transaction);

      // ② 本地生成 4 字武侠风昵称（允许重名）
      nickname = generateNickname();

      // ③ 写入 user 集合
      const userColTx = transaction.collection('user');
      await userColTx.add({
        data: {
          id: newId,
          nickname: nickname,
          create_time: now,
          update_time: now,
          open_id: openId,
        },
      });

      // ④ 提交事务
      await transaction.commit();
    } catch (txErr) {
      // 事务异常回滚
      await transaction.rollback();
      throw txErr;
    }

    return {
      code: 0,
      msg: '注册成功',
      isNew: true,
      data: {
        id: newId,
        nickname: nickname,
        create_time: now,
        update_time: now,
        open_id: openId,
      },
    };
  } catch (err) {
    console.error('login 云函数异常:', err);
    return {
      code: -1,
      msg: err.message || '服务器内部错误',
    };
  }
};
