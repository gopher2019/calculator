function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * 本地随机生成武侠风昵称（纯计算，零外部依赖）
 * 风格 A（50%）：复姓 + 双字雅词，如「慕容听雨」「独孤踏雪」
 * 风格 B（50%）：双字雅词 + 双字雅词，如「听雨观云」「踏雪寻梅」
 * 允许重名，不做查重
 *
 * @returns {string} 4 字武侠风昵称
 */
function generateNickname() {
  const ming = pick(WUXIA_MING);
  if (Math.random() < 0.5) {
    // 风格 B：雅词 + 雅词（避免与自身重复）
    let ming2 = pick(WUXIA_MING);
    while (ming2 === ming) {
      ming2 = pick(WUXIA_MING);
    }
    return ming + ming2;
  }
  // 风格 A：复姓 + 雅词
  return pick(WUXIA_XING) + ming;
}

